    //Metodo para criar uma linha
    public boolean create  (ModelProduto prod){
       con = Conection.getConnection();
       String sql = "INSERT INTO prod (cod, nome, quant, tipo) VALUES (?,?,?,?)"; 
       PreparedStatement stmt = null;
       
        try {
            stmt = con.prepareStatement(sql);
            stmt.setString(1, prod.getCod());
            stmt.setString(2, prod.getNome());
            stmt.setString(3, prod.getQuant());
            stmt.setString(4, prod.getTipo());
            stmt.executeUpdate();
            return true;
        } catch (SQLException ex) {
            System.err.println("erro" + ex);
            return false;
        }finally{
            Conection.closeConnection(con, stmt);
        }
    } 
