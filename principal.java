    //Método para carregar os dados do banco de dados
    public void carregaTabela(){
        DefaultTableModel modelo = (DefaultTableModel) Tb.getModel();
        modelo.setNumRows(0);
        
        try{
            con = Conection.getConnection();
            PreparedStatement pstm;
            ResultSet rs;
            
            pstm = con.prepareStatement("select * from prod;");
            rs = pstm.executeQuery();
            
            while(rs.next()){
                modelo.addRow(new Object[]{
                    rs.getInt(1),
                    rs.getString(2),
                    rs.getInt(3),
                    rs.getString(4)
                });
            }            
            Conection.closeConnection(con, pstm, rs);
        }catch(Exception ErroSql){
            JOptionPane.showMessageDialog(null, "Erro"+ErroSql, "ERRO", JOptionPane.ERROR_MESSAGE);
        }
        
    }
